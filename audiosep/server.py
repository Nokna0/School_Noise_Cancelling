# ============================================================
# audiosep/server.py
# ------------------------------------------------------------
# 브라우저(클라이언트)로부터 마이크에서 캡쳐한 PCM 오디오
# 청크를 WebSocket 으로 받아서, "잡음 후보" 신호를 만들어
# 다시 클라이언트로 돌려주는 아주 단순한 데모 서버.
#
# 실제 AudioSep(딥러닝 기반 음원 분리 모델)을 붙이기 전에
# 통신 파이프라인이 잘 도는지 확인하기 위한 "가짜" 버전이라
# 함수 이름이 fake_audiosep 으로 되어 있다.
# 여기서는 고주파를 잘라 버리는 단순 저역통과 필터로 대신한다.
#
# 통신 규약:
#   • 입력 : float32 PCM, 16 kHz, mono (브라우저가 보내는 그대로)
#   • 출력 : 같은 길이의 float32 PCM (잡음 추정치 역할)
# ============================================================

import asyncio          # async/await 기반 이벤트 루프
import websockets       # WebSocket 서버 라이브러리
import numpy as np      # FFT 와 배열 변환에 사용


# ============================================================
# 가짜 AudioSep
# ------------------------------------------------------------
# 입력 오디오에서 "노이즈 같은 성분"을 만들어 내는 역할.
# 진짜 모델을 붙이기 전까지의 임시 구현이다.
#
# 동작:
#   1) 실수 입력을 FFT 하여 주파수 영역으로 보낸다.
#   2) 3000 번 째 빈(bin) 이후 고주파를 모두 0 으로 만든다.
#      → 저역통과 필터(LPF) 효과.
#   3) 역 FFT 로 다시 시간 영역으로 돌린다.
#
# 결과적으로 음성의 저주파 성분만 남는데, NLMS 입장에서는
# "잡음 참조 신호 x" 로 활용할 수 있다.
# ============================================================
def fake_audiosep(audio):

    # 실수 신호에 대한 FFT (음수 주파수가 대칭이므로 절반만 반환).
    fft = np.fft.rfft(audio)

    # 자르고 싶은 주파수 빈 인덱스. (정확한 Hz 값은
    # 입력 길이/샘플레이트에 따라 달라지므로 어림 잡은 값.)
    cutoff = 3000

    # 고주파 영역을 0 으로 → 사실상 저역통과 필터.
    fft[cutoff:] = 0

    # 역 FFT 로 시간 영역 신호 복원.
    separated = np.fft.irfft(fft)

    # 클라이언트가 Float32Array 로 받을 수 있도록 dtype 맞춰서 반환.
    return separated.astype(np.float32)


# ============================================================
# websocket handler
# ------------------------------------------------------------
# 클라이언트 한 명이 연결될 때마다 실행되는 코루틴.
# 무한히 메시지를 받아 fake_audiosep 결과를 돌려보낸다.
#
# 메시지 형식은 바이너리(arraybuffer)이며, 그 안에는
# float32 샘플들이 연속으로 들어 있다고 가정한다.
# ============================================================
async def handler(websocket):

    print("클라이언트 연결됨")

    # 연결이 끊길 때까지 들어오는 메시지를 순서대로 처리.
    async for message in websocket:

        # 받은 바이트열을 float32 배열로 해석.
        audio = np.frombuffer(

            message,
            dtype=np.float32

        )

        # 가짜 AudioSep 으로 잡음 후보 신호 생성.
        x = fake_audiosep(audio)

        # 같은 형식(float32 바이트)으로 돌려보낸다.
        # 클라이언트의 NLMS 가 이 결과를 "참조 신호 x" 로 사용한다.
        await websocket.send(

            x.tobytes()

        )


# ============================================================
# 서버 시작
# ------------------------------------------------------------
# 0.0.0.0:8765 에서 대기. (로컬 테스트는 localhost:8765 로 접속)
# asyncio.Future() 를 await 함으로써 영원히 멈추지 않게 한다.
# Ctrl+C 로 종료할 때까지 서버가 살아 있다.
# ============================================================
async def main():

    async with websockets.serve(

        handler,        # 연결마다 실행할 코루틴
        "0.0.0.0",     # 모든 네트워크 인터페이스에서 수신
        8765            # 사용할 포트 번호

    ):

        print("서버 실행 중")

        # "끝나지 않는 future" 를 기다리며 서버를 계속 살려 둔다.
        await asyncio.Future()


# 모듈 직접 실행 시 이벤트 루프 시작.
asyncio.run(main())
